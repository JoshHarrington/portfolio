           <h1 class="title">Why is the &#39;independent&#39; media intent on destroying the reputation of Russell Brand?</h1>
           
           <div class="blogListItem">
               <div class="blogImg brand wide">
                   <span class="bgImg" data-img-source="http://i.guim.co.uk/static/w-1430/h--/q-95/sys-images/Guardian/Pix/pictures/2014/10/8/1412761498119/Russell-Brand-017.jpg"></span>               
               </div>
               <p>First posted on <a target="_blank" href="https://medium.com/@josh_harrington/why-is-the-media-left-intent-on-destroying-the-reputation-of-russell-brand-7002f880e5d5">medium</a>.</p>
               <p>Russell Brand is an important figure in politics today, whether you agree with his viewpoints or not, he’s important.</p>
               <p>The reason that he’s important is because he is able to publicly empathise with a huge section of the population that for the longest time has been ignored and vilified by the centre right. However instead of embracing him as the advantage to the left that he is, the left and independent media has begun a campaign to undermine and demonise him.</p>
               <p>Anything of value that he brings to a conversation is ignored and instead his critics focus upon his <a target="_blank" href="http://www.theguardian.com/commentisfree/2014/oct/24/britain-russell-brand-revolution-newsnight">mannerisms</a>, his <a target="_blank" href="http://www.independent.co.uk/news/people/news/the-iiosi-smuggest-britons-list-1772300.html?action=gallery&ino=6">dress sense (and weight, apparently)</a>, his money, his <a target="_blank" href="http://www.independent.co.uk/news/people/russell-brand-admits-hes-open-minded-to-911-conspiracy-theories-in-combative-newsnight-interview-with-evan-davis-9815531.html">open-mindedness</a>. People <a target="_blank" href="https://yougov.co.uk/news/2014/11/12/public-revolt-russell-brand/">fervently</a> jump on <a target="_blank" href="https://d25d2506sfb94s.cloudfront.net/cumulus_uploads/document/wg9oc0ppuy/InternalResults_141111_celebrities_and_politics_Website.pdf">statistics</a> to prove that he is disliked.</p>

                <blockquote>Russell Brand is disliked and, more than other celebrities, seen as having a negative influence on political debate</blockquote>
                <p>We see someone would could be a huge asset in an open debate about the state of politics today and so we try to squash him.</p>

                <p>Am I missing something here?</p>

                <p>Is there something that I just don’t get about this person? Is there a reason that we need to demonise him? Is he dangerous?</p>

<p>I pride myself on good judgement and empathy. I spend most of my time trying to imagine being the person using the products that I’m building. This angles me towards seeing things from different perspectives.</p>

<p>Although this is meant to be an impartial comment on the conversation around Brand at the moment I need to spend a moment considering the people that can’t get on board with Brand’s brand of humour and politics. By judging him on the way that he looks, or for his sexuality, this is not going to encourage an open conversation. For far too long our thoughts were set upon the backwards notion that how someone looks or talks were important considerations about the worthiness of them or their opinions.</p>

<p>It is with <a target="_blank" href="http://i100.independent.co.uk/article/9-things-we-learned-from-russell-brands-newsnight-interview--gkhIlzDc8g">glee and smugness</a> that the media, those that you could have imagined would support Brand’s points of view, report on the <a target="_blank" href="http://i100.independent.co.uk/article/people-are-now-trolling-russell-brand-by-tweeting-graphs-at-him--xklpGtw9Lx">jokes made at his expense</a>.</p>

<p>You will have noticed that I made links to the <a target="_blank" href="http://www.independent.co.uk/">Independent</a> (and their magazine-ified little brother <a target="_blank" href="http://i100.independent.co.uk/">i100</a>) several times during this short comment and there’s reason for that. They get the emphasis wrong again and again, they do not need to try and fit into the niche that <a target="_blank" href="http://www.buzzfeed.com/">Buzzfeed</a> have carved for themselves. They need to follow their name’s suggestion and be independent, they don’t need to push public opinion in any particular direction.</p>

<p>Instead, they could just present the facts.</p>
