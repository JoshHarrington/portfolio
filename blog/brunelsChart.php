               <p>From Summer 2010 to Summer 2011 I was the Music Manager for Brunel University's radio station, Radio Brunel. During my time I compiled a large music database of radio suitable songs and brought together a team of people who were passionate about music to help me turn around the music that the station put out.</p>

<p>The whole station is voluntary. DJs giving up time to plan shows and then sit in a very warm room for the student body, most of whom do not know of the station's existence. It is a thankless job but that's not why we do it. For me, it was for the love of the music, being with friends and sometimes really useful work experience. My one regret from my time as Music Manager, which overall was a great experience, was that I did not get my plan for a student chart into action.</p>
 
<p>The point of this quick entry is just to outline the idea behind the chart, so that if someone wanted to pick up the concept and run with it, they wouldn't have to start from scratch.</p>
 
<p>The principle of the chart was: "why not have a chart for the Brunel student body, chosen and voted for by the students". Students would send in suggestions for the chart to an email address after that week's Chart had been on the radio. For example the Chart could be on a Sunday, so the suggestions could come in at 8pm on the Monday evening. The first fifty unique songs received would be put into the Chart, so from Tuesday morning onwards there would be an interactive list of fifty songs on the station's site, so the students could vote for their favourite song.</p>
 
<p>The idea of the site's song list, was for you to be able to see the current order of the songs, number of votes each song has and also the song moving - as a direct result of your vote (you would get to 'hart' the song you liked, this would clearly needed to be limited so that the site made some attempt to limit users trying to vote many times). This moving feature would only be in use on the site for the first three days, so that for the last three (Friday-Sunday) the songs would be alphabetically ordered. This reduced visible movement, could be seen as a negative initially, but as long as it was made clear that this was so that the order of the chart is kept secret until the top 20 (or top 15) are played on the chart show (possibly with the student who suggested the number one introducing it).</p>
 
<p>This format may work better with a longer time between the songs suggested and them being put on the site, giving the online staff more time. The only downfall in this theory is there is less time for the students to vote for the chart.</p>
 
<p>It was hoped that after a couple of weeks the chart would pick up a following, and with more people suggesting songs, increase the variety of songs to be picked from. One concern was that the chart may simply start reflecting the current chart as put out by Radio 1, however, I think with the right encouragement it could be seen as a really independent musical expression of the University.</p>
 
<p>The last feature was to heavily involve the station's DJs, so they could get their song into the chart, or they could pick a song that they really liked and back it. If it made number one then they could present the chart show or guest present with the standard presenter. This idea of DJs backing a song would work in two ways: firstly by them talking about the chart show on their show, they would get their audience engaged; secondly, they would create some real competition between the songs, otherwise there could be less interesting battles for the top spot.</p>
 
<p>Hope you enjoyed my explanation into how the chart show would work, and I hope that someone decides it's worth being put into practice!</p>
