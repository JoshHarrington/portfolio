               <p><span>Problem:</span>The Earth's surface area is 51.0 Gigahectares (Gha), the Oceans cover 36.1 Gha and Land covers 14.8 Gha. Today we use 4.9 Gha of the 10 Gha that can sustain life, for agriculture. Population is set to the increase to 9 billion by 2050 (Hans Rosling, 2010). The standard reaction to increased population size in our past has always been to increase the scale of agriculture to meet demand. This can no longer be considered an option, further damage to the natural ecosystems as a result of increasing the scale of agriculture will likely cause irrevocable damage to the planet.</p>
               <p>Detailed in Global Food: Waste Not, Want Not (IMechE, 2013), diets in rapidly developing countries are becoming more meat based. China's meat consumption per capita in cities rose from 20kg to 29kg per year between 1981 and 2004. If this trend of increased meat consumption in developing countries continues, this could mean that by 2050 up to 8.83 Gha of the 10 Gha that can support life would be in use for food production to meet global demand (IMechE, 2013).</p>
 
<p><span>Possible solutions:</span>One method of reducing the area taken by agriculture would be to change the world's diet. If all nations had low-meat diets and high agricultural efficiency space needed for agriculture could reduce to 4.13 Gha by 2050 (IMechE, 2013).</p>

<p>However, it is the belief of this report that in a situation where there was no chronic undernourishment, as exists for one in eight people today (FAO, 2013), people should have the freedom to eat what they want. There should be no imposition on them by any nation or global body, to restrict that freedom of choice.</p>

<p>Solutions should allow people to eat what they want, but also lead to reduced waste of resources. One solution that fits into this criteria is lab grown meat. Predicted by Winston Churchill in the 1930s, meat can be grown separate from an animal. Not only could this stop animals being raised with the sole purpose of being killed for us to eat, there may also be huge reductions in resource use. “Cultured [lab grown] meat could potentially be produced with up to 96% lower greenhouse gas emissions, 45% less energy, 99% lower land use, and 96% lower water use than conventional meat.” - Hanna Tuomisto of Oxford University’s Wildlife Conservation Research Unit (The Guardian, 2012).</p>

<p>Much progress is being made to make lab grown meat a reality, but it needs more funding and more attention. Leading the charge in bringing lab grown meat to the consumer are organisations like PETA (People for the Ethical Treatment of Animals) who offered $1 Million in 2008 to the first laboratory that could bring lab grown meat to market (PETA, 2012).</p>

<p>Another solution that puts no restriction on what people eat, whilst reducing resource use is reducing waste, specifically food waste. The IMechE stated this year that up to 50% of all food produced is wasted.</p>

<p>Specific to post-industrial nations like the UK, waste mainly happens with the retailer and consumer. Supermarkets are partly to blame, refusing to stock produce below a set aesthetic level and using bulk offers to shift the waste onto consumers (BBC, 2013). Consumers are also to blame with 30 - 50% of what has been purchased, in post-industrial nations, being thrown away (IMechE, 2013).</p>

<p>Is it the belief of this report that consumers should not be berated for household food waste, instead they should have tools that help them reduce food waste and save money. If consumer tools such as this could be integrated into supermarket solutions where food is only grown when it needed and wanted by consumers, the waste produced at the retail stage could be massively decreased.</p>

<p>In conclusion, we have a decision to make. We must choose between reducing the areas taken by agriculture and making every effort to encourage growth of natural ecosystems; or instead choose to continue as we are and reduce the areas taken by natural ecosystems to 1.17 Gha, 23% of today’s size. It is likely that the reduction of the area available to natural ecosystems has already done untold damage, but with efforts to increase both the size of these natural areas and the life that these areas can sustain, some of the effects could be mitigated.</p>

